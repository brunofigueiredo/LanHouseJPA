    private void botaoSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoSalvarActionPerformed
        Cliente m = new Cliente();        
            m.setId(Integer.parseInt(txtId.getText()));
            m.setNome(txtNome.getText());
            m.setCpf(txtCpf.getText());
            m.setRg(txtRg.getText()); 
            m.setCidade(txtCid.getText()); 
         
             try {
            m.setDatanasc(new SimpleDateFormat("dd/MM/yyyy").parse(txtDtNacs.getText()));
        } catch (ParseException ex) {
            System.out.println("Erro na conversão de datas" + ex.getMessage());
        }
            m.setEndereco(txtEndereco.getText()); 
            m.setEstado(txEstado.getText());
            m.setAtendente((Atendente)cbAtendente.getSelectedItem());
                       
            ClienteController mc = new ClienteController();
            if (mc.salvar(m)){         
            model.updateCliente(linhaSelecionada, m);
            }
            dispose();
    }//GEN-LAST:event_botaoSalvarActionPerformed

