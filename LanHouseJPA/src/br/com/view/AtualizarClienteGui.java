private void carregarCombo() {
         //assim como é feito numa JTable, temos o DefaultComboBoxModel que é o model do JComboBox
        DefaultComboBoxModel comboModel = (DefaultComboBoxModel) cbAtendente.getModel();
        //removendo todos os elementos do combo
        comboModel.removeAllElements();
        //cria a lista: java.util.List
        List<Atendente> atendentes = new ArrayList<Atendente>();
        AtendenteController a = new AtendenteController();
        atendentes = a.listarAtendente();
        
     
        for (int linha = 0; linha < atendentes.size(); linha++) {
            //pegando a categoria da lista
            Atendente atendente = atendentes.get(linha);
            //adicionando a categoria no combo
            comboModel.addElement(atendente);
        }
    }
